nombres = ['naty', 'osvaldo', 'lil', 'ariel']

print(len(nombres)) # preguntar cuantos elementos tiene una lista

nombres.append('marcelo') # añadir un elemento

nombres.insert(2, 'pedro') # añadir un elemento en un indice especifico

nombres.remove('naty') # eliminar un elemento

nombres.pop() # eliminar el ultimo elemento

del nombres[2] # eliminar un elemento en un indice especifico

nombres.clear() # eliminar todos los nombres

del nombres # eliminar la lista